!set plsqlUseSlash true
CREATE OR REPLACE FUNCTION CUST.F_IDS_GET_TABLENAME(
  I_TABLENAME IN STRING, 
  I_KHH IN STRING
) 
RETURN STRING 
IS
V_DBNAME STRING;
BEGIN
  V_DBNAME := 'TEMPSPARK.';
  IF I_KHH IS NULL OR I_KHH = '' THEN
    RETURN V_DBNAME || I_TABLENAME;
  ELSE
    RETURN V_DBNAME || I_TABLENAME || '_' || I_KHH;
  END IF;
END;
/
